getgenv().AutoPlanetsRobloxiaCentauri = state

spawn(function()
    while AutoPlanetsRobloxiaCentauri == true do
        local args = {
        [1] = workspace.World:FindFirstChild("Robloxia Centauri").orbitingBodies.Grimm,
        [2] = -2.e+22
                     }
        game:GetService("ReplicatedStorage").ChangePlanetMass:FireServer(unpack(args))
        wait(5)
    end
end)

